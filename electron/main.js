// --- Download Thumbnail ---
ipcMain.handle(
  "download-thumbnail",
  async (_, { thumbnailUrl, title, savePath }) => {
    return new Promise((resolve, reject) => {
      const https = require("https");
      const http = require("http");
      const sanitized = title.replace(/[/\\?%*:|"<>]/g, "-").trim();
      const dest = path.join(savePath, `${sanitized}.jpg`);
      const file = fs.createWriteStream(dest);
      const client = thumbnailUrl.startsWith("https") ? https : http;
      client
        .get(thumbnailUrl, (res) => {
          res.pipe(file);
          file.on("finish", () => {
            file.close();
            resolve(dest);
          });
        })
        .on("error", (err) => {
          fs.unlink(dest, () => {});
          reject(err);
        });
    });
  },
);

// --- Download ---
ipcMain.handle(
  "download",
  async (
    event,
    {
      url,
      formatId,
      container,
      savePath,
      clipStart,
      clipEnd,
      audioOnly,
      audioQuality,
    },
  ) => {
    return new Promise((resolve, reject) => {
      const win = BrowserWindow.fromWebContents(event.sender);

      let args;

      if (audioOnly) {
        // Audio-only download
        const quality = audioQuality || "192";
        args = [
          "-f",
          "bestaudio/best",
          "--extract-audio",
          "--audio-format",
          "mp3",
          "--audio-quality",
          `${quality}k`,
          "--ffmpeg-location",
          getFfmpegDir(),
          "--js-runtimes",
          "node",
          ...cookieArgs(),
          ...(clipStart && clipEnd
            ? [
                "--download-sections",
                `*${clipStart}-${clipEnd}`,
                "--force-keyframes-at-cuts",
              ]
            : []),
          "-o",
          path.join(savePath, "%(title)s.%(ext)s"),
          "--newline",
          url,
        ];
      } else {
        // Video download
        args = [
          "-f",
          formatId,
          "--merge-output-format",
          container,
          "--ffmpeg-location",
          getFfmpegDir(),
          "--js-runtimes",
          "node",
          ...cookieArgs(),
          ...(clipStart && clipEnd
            ? [
                "--download-sections",
                `*${clipStart}-${clipEnd}`,
                "--force-keyframes-at-cuts",
              ]
            : []),
          "-o",
          path.join(savePath, "%(title)s.%(ext)s"),
          "--newline",
          url,
        ];
      }

      const proc = spawn(getYtDlpPath(), args);
      activeDownload = proc;

      proc.stdout.on("data", (data) => {
        const line = data.toString();
        console.log(line);
        const match = line.match(/\[download\]\s+([\d.]+)%/);
        if (match) {
          const percent = Math.round(parseFloat(match[1]));
          win.webContents.send("download-progress", percent);
        }
      });

      proc.stderr.on("data", (d) => console.error(d.toString()));
      proc.on("error", (err) => {
        activeDownload = null;
        reject(new Error(`Failed to start yt-dlp: ${err.message}`));
      });
      proc.on("close", (code) => {
        activeDownload = null;
        if (code === 0) resolve();
        else reject(new Error(code === null ? "cancel" : "Download failed"));
      });
    });
  },
);

// --- Cancel Download ---
ipcMain.handle("cancel-download", () => {
  if (activeDownload) {
    activeDownload.kill("SIGTERM");
    activeDownload = null;
  }
});
